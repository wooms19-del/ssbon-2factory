-- ============================================================
-- 순수본 2공장 품목 마스터 + BOM (TFT 통합 DB용)
-- ERP 코드 기준. item_code = 회사 공식 코드(ERP)
-- ============================================================

-- 1) 품목 구분 코드 마스터
CREATE TABLE item_category (
  category_code  VARCHAR(20) PRIMARY KEY,
  category_name  VARCHAR(40) NOT NULL UNIQUE
);

-- 2) 품목 마스터
CREATE TABLE item_master (
  item_id        INT          PRIMARY KEY AUTO_INCREMENT,
  item_code      VARCHAR(20)  NOT NULL UNIQUE,          -- ERP 코드 (UK)
  item_name      VARCHAR(100) NOT NULL,
  spec           VARCHAR(40),
  unit           VARCHAR(10)  NOT NULL,
  category_code  VARCHAR(20)  NOT NULL,
  part           VARCHAR(10),                           -- 홍두께/설도/우둔 (NULL 가능)
  is_active      BOOLEAN      NOT NULL DEFAULT TRUE,
  CONSTRAINT fk_item_category FOREIGN KEY (category_code) REFERENCES item_category(category_code)
);

-- 3) BOM (모품목이 자품목을 정미수량만큼 소요)
CREATE TABLE bom (
  bom_id         INT          PRIMARY KEY AUTO_INCREMENT,
  parent_code    VARCHAR(20)  NOT NULL,                 -- 모품목 (item_master.item_code 참조)
  child_code     VARCHAR(20)  NOT NULL,                 -- 자품목
  net_qty        DECIMAL(12,4) NOT NULL,                -- 정미수량
  unit           VARCHAR(10)  NOT NULL,
  CONSTRAINT fk_bom_parent FOREIGN KEY (parent_code) REFERENCES item_master(item_code),
  CONSTRAINT fk_bom_child  FOREIGN KEY (child_code)  REFERENCES item_master(item_code),
  CONSTRAINT uk_bom UNIQUE (parent_code, child_code)    -- 같은 모-자 조합 중복 방지
);

